configure_git_identity() {
  local env_name env_email env_signing env_signing_set
  env_name="${GIT_USER_NAME:-}"
  env_email="${GIT_USER_EMAIL:-}"
  env_signing="${GIT_USER_SIGNINGKEY:-}"
  env_signing_set="${GIT_USER_SIGNINGKEY+set}"

  local file_name file_email file_signing
  if [[ -f "$GIT_LOCAL_CONFIG_FILE" ]]; then
    file_name="$(git config --file "$GIT_LOCAL_CONFIG_FILE" --get user.name 2>/dev/null || true)"
    file_email="$(git config --file "$GIT_LOCAL_CONFIG_FILE" --get user.email 2>/dev/null || true)"
    file_signing="$(git config --file "$GIT_LOCAL_CONFIG_FILE" --get user.signingkey 2>/dev/null || true)"
  fi

  local existing_name existing_email existing_signing
  existing_name="$(git config --global --get user.name 2>/dev/null || true)"
  existing_email="$(git config --global --get user.email 2>/dev/null || true)"
  existing_signing="$(git config --global --get user.signingkey 2>/dev/null || true)"

  # Prefer saved local config, then env overrides, then current global git, then hardcoded fallback.
  # Treat empty strings as unset.
  local default_name
  if [[ -n "${file_name:-}" ]]; then
    default_name="$file_name"
  elif [[ -n "${env_name:-}" ]]; then
    default_name="$env_name"
  elif [[ -n "${existing_name:-}" ]]; then
    default_name="$existing_name"
  else
    default_name="$DEFAULT_GIT_NAME"
  fi

  local default_email
  if [[ -n "${file_email:-}" ]]; then
    default_email="$file_email"
  elif [[ -n "${env_email:-}" ]]; then
    default_email="$env_email"
  elif [[ -n "${existing_email:-}" ]]; then
    default_email="$existing_email"
  else
    default_email="$DEFAULT_GIT_EMAIL"
  fi

  local default_signing
  if [[ "$env_signing_set" == "set" ]]; then
    default_signing="$env_signing" # allow explicit empty to disable signing
  elif [[ -n "${file_signing:-}" ]]; then
    default_signing="$file_signing"
  elif [[ -n "${existing_signing:-}" ]]; then
    default_signing="$existing_signing"
  else
    default_signing="$DEFAULT_GIT_SIGNINGKEY"
  fi

  local final_name="${env_name:-$default_name}"
  local final_email="${env_email:-$default_email}"
  local final_signing="${env_signing:-$default_signing}"

  if [[ "${DOTFILES_SKIP_GIT_PROMPTS:-0}" == "1" ]]; then
    if [[ -z "$default_name" && -z "$default_email" ]]; then
      print_info "Skipping Git identity prompts (DOTFILES_SKIP_GIT_PROMPTS=1) with no defaults; leaving existing config untouched."
      return
    fi
    print_info "Skipping Git identity prompts (DOTFILES_SKIP_GIT_PROMPTS=1); using provided defaults."
  elif ! has_tty; then
    if [[ -z "$default_name" && -z "$default_email" ]]; then
      print_info "No TTY and no defaults available; skipping Git identity configuration."
      return
    fi
  else
    if [[ -z "$env_name" || -z "$env_email" ]]; then
      local name_prompt="Git user.name"
      final_name="$(prompt_with_default "$name_prompt" "$final_name")"
      while has_tty && [[ -z "$final_name" ]]; do
        print_warning "Git user.name is required."
        final_name="$(prompt_with_default "$name_prompt" "$final_name")"
      done

      local email_prompt="Git user.email"
      final_email="$(prompt_with_default "$email_prompt" "$final_email")"
      while has_tty && [[ -z "$final_email" ]]; do
        print_warning "Git user.email is required."
        final_email="$(prompt_with_default "$email_prompt" "$final_email")"
      done

      local signing_prompt="Git signingkey (optional)"
      final_signing="$(prompt_with_default "$signing_prompt" "$final_signing")"
    else
      print_info "Using Git identity defaults without prompting (env provided)."
    fi
  fi

  if [[ -z "$final_name" || -z "$final_email" ]]; then
    print_warning "Git identity not updated (missing name/email)."
    return
  fi

  mkdir -p "$(dirname "$GIT_LOCAL_CONFIG_FILE")"
  touch "$GIT_LOCAL_CONFIG_FILE"

  # Update only the managed keys and preserve all other local custom settings.
  git config --file "$GIT_LOCAL_CONFIG_FILE" user.name "$final_name"
  git config --file "$GIT_LOCAL_CONFIG_FILE" user.email "$final_email"

  if [[ -n "$final_signing" ]]; then
    git config --file "$GIT_LOCAL_CONFIG_FILE" user.signingkey "$final_signing"
    git config --file "$GIT_LOCAL_CONFIG_FILE" commit.gpgsign true
    git config --file "$GIT_LOCAL_CONFIG_FILE" tag.gpgSign true
    git config --file "$GIT_LOCAL_CONFIG_FILE" tag.forceSignAnnotated true
  else
    git config --file "$GIT_LOCAL_CONFIG_FILE" --unset-all user.signingkey 2>/dev/null || true
    git config --file "$GIT_LOCAL_CONFIG_FILE" commit.gpgsign false
    git config --file "$GIT_LOCAL_CONFIG_FILE" tag.gpgSign false
    git config --file "$GIT_LOCAL_CONFIG_FILE" tag.forceSignAnnotated false
  fi

  print_success "Git identity configured in $GIT_LOCAL_CONFIG_FILE"
  print_info "Git user.name: $final_name"
  print_info "Git user.email: $final_email"
  if [[ -n "$final_signing" ]]; then
    print_info "Git signingkey: $final_signing"
  fi
}

