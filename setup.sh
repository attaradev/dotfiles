if [[ -t 1 && -z "${NO_COLOR:-}" ]]; then
  RED=$'\033[38;5;203m'
  GREEN=$'\033[38;5;76m'
  YELLOW=$'\033[38;5;220m'
  BLUE=$'\033[38;5;69m'
  MAGENTA=$'\033[38;5;171m'
  CYAN=$'\033[38;5;45m'
  NC=$'\033[0m' # No Color
else
  RED=""
  GREEN=""
  YELLOW=""
  BLUE=""
  MAGENTA=""
  CYAN=""
  NC=""
fi

# Helper functions
print_header() {
  echo ""
  echo -e "${MAGENTA}============================================${NC}"
  echo -e "${MAGENTA}$1${NC}"
  echo -e "${MAGENTA}============================================${NC}"
  echo ""
}

print_success() {
  echo -e "${GREEN}✓${NC} $1"
}

print_error() {
  echo -e "${RED}✗${NC} $1"
}

print_warning() {
  echo -e "${YELLOW}⚠${NC} $1"
}

print_info() {
  echo -e "${BLUE}ℹ${NC} $1"
}

print_note() {
  echo -e "${CYAN}ℹ${NC} $1"
}

has_tty() {
  [[ -t 0 || -t 1 || -t 2 ]]
}

can_read_tty() {
  [[ -r /dev/tty ]] || return 1
  : </dev/tty 2>/dev/null
}

prompt_yes_no() {
  local prompt="$1"
  local default_answer="${2:-n}"
  local suffix="[y/N]"
  local default="n"

  if [[ "$default_answer" =~ ^[Yy]$ ]]; then
    suffix="[Y/n]"
    default="y"
  fi

  if has_tty && can_read_tty; then
    read -r -p "$(printf "%s %s " "$prompt" "$suffix")" answer </dev/tty || answer="$default"
    answer="${answer:-$default}"
  else
    answer="$default"
  fi

  [[ "$answer" =~ ^[Yy]$ ]]
}

prompt_with_default() {
  local prompt="$1"
  local default_value="${2:-}"
  local suffix=""
  local answer="$default_value"

  if [[ -n "$default_value" ]]; then
    suffix=" [$default_value]"
  fi

  if has_tty && can_read_tty; then
    read -r -p "$(printf "%s%s: " "$prompt" "$suffix")" input </dev/tty || input=""
    if [[ -n "$input" ]]; then
      answer="$input"
    fi
  fi

  echo "$answer"
}

normalize_bool() {
  local raw
  raw="$(printf '%s' "${1:-}" | tr '[:upper:]' '[:lower:]')"
  case "$raw" in
    1|y|yes|true|on) echo "1" ;;
    0|n|no|false|off) echo "0" ;;
    *) echo "0" ;;
  esac
}

